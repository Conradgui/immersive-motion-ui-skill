#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";
import { validateManifestData } from "../skills/immersive-motion-ui/scripts/validate-capability-manifest.mjs";

const args = process.argv.slice(2);
const pretty = args.includes("--pretty");
const installTarget = readOption("--install-target");
const requireInstalled = args.includes("--require-installed");
const repoRoot = path.resolve(new URL("..", import.meta.url).pathname);
const manifestPath = path.join(repoRoot, "core-release-manifest.json");

const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
const checks = [];
const skillSourceRoot = path.join(repoRoot, manifest.skill_path || "");
const capabilityManifestPath = path.join(skillSourceRoot, "capability-manifest.json");
const capabilityManifest = validateCapabilityManifest(capabilityManifestPath, skillSourceRoot);

check("manifest.exists", true, "core-release-manifest.json is readable");
check("manifest.codexOnly", manifest.release_scope === "codex-only-first-release", `release_scope=${manifest.release_scope}`);
check("manifest.libraryOptional", manifest.library_dependency?.required === false, "library_dependency.required must be false");
check("capabilityManifest.valid", capabilityManifest.status === "PASS", capabilityManifest.status === "PASS" ? "capability manifest passed" : `${capabilityManifest.failureCount} capability checks failed`);

for (const includePath of manifest.include || []) {
  const resolved = path.join(repoRoot, includePath);
  check(`include.${includePath}`, fs.existsSync(resolved), `${includePath} must exist`);
}

for (const excludePath of manifest.exclude || []) {
  if (excludePath === "multi-platform mirrors") continue;
  const resolved = path.join(repoRoot, excludePath);
  check(`exclude.${excludePath}`, !fs.existsSync(resolved), `${excludePath} must not exist in Core package`);
}

const skillPath = path.join(skillSourceRoot, "SKILL.md");
const skillText = fs.readFileSync(skillPath, "utf8");
const frontmatter = parseFrontmatter(skillText);
check("skill.frontmatter.onlyNameDescription", sameSet(Object.keys(frontmatter), ["name", "description"]), "SKILL.md frontmatter must only contain name and description");
check("skill.name", frontmatter.name === manifest.skill_name, `frontmatter name=${frontmatter.name}`);
check("skill.noLibraryHardDependency", !/prompts\/|Pro prompts|assets\/|demos\/|library-manifest\.json/.test(skillText), "SKILL.md must not require Library paths");

const forbiddenHits = findForbiddenHits(repoRoot, [
  "prompts/",
  "Pro prompts",
  "assets/",
  "demos/",
  "Generated by MotionSites",
  "VIRALMEDIA",
  "ClearInvoice",
]);
check("core.noForbiddenMarkers", forbiddenHits.length === 0, forbiddenHits.length ? forbiddenHits.join("; ") : "no forbidden markers found");

const installPlan = installTarget ? buildInstallPlan(repoRoot, installTarget, manifest) : null;
const installTargetStatus = installPlan ? inspectInstallTarget(installPlan) : null;
const packageFingerprint = buildPackageFingerprint(skillSourceRoot, manifest.skill_path || "");
if (installTarget && requireInstalled) {
  check("installTarget.exists", installTargetStatus.destinationExists, "destination skill directory must exist");
  check("installTarget.matchesPackage", installTargetStatus.matchesPackage, "installed files must match source package fingerprints");
}
const report = {
  status: checks.some((item) => !item.pass) ? "FAIL" : "PASS",
  dryRun: true,
  repoRoot,
  installTarget: installTarget || null,
  installPlan,
  installTargetStatus,
  packageFingerprint,
  capabilityManifest,
  manifest: {
    package: manifest.package,
    release_scope: manifest.release_scope,
    skill_name: manifest.skill_name,
    skill_path: manifest.skill_path,
  },
  checks,
  failureCount: checks.filter((item) => !item.pass).length,
};

console.log(JSON.stringify(report, null, pretty ? 2 : 0));
process.exit(report.failureCount ? 1 : 0);

function check(id, pass, detail) {
  checks.push({ id, pass: Boolean(pass), detail });
}

function validateCapabilityManifest(manifestFile, skillRoot) {
  try {
    const data = JSON.parse(fs.readFileSync(manifestFile, "utf8"));
    return validateManifestData(data, { skillRoot, manifestPath: manifestFile });
  } catch (error) {
    return {
      status: "FAIL",
      manifest: manifestFile,
      skillRoot,
      checks: [{ id: "manifest.read", pass: false, detail: error.message }],
      failureCount: 1,
    };
  }
}

function readOption(name) {
  const equalForm = args.find((item) => item.startsWith(`${name}=`));
  if (equalForm) return equalForm.slice(name.length + 1);
  const index = args.indexOf(name);
  if (index === -1) return null;
  return args[index + 1] || null;
}

function buildInstallPlan(root, target, manifestData) {
  const targetRoot = path.resolve(target);
  const sourceRoot = path.join(root, manifestData.skill_path || "");
  const destinationRoot = path.join(targetRoot, manifestData.skill_name || path.basename(sourceRoot));
  return {
    mode: "dry-run",
    sourceRoot,
    destinationRoot,
    wouldCreateDirectories: uniqueSorted(
      collectFiles(sourceRoot).map((file) => path.dirname(path.join(destinationRoot, path.relative(sourceRoot, file))))
    ),
    wouldCopyFiles: collectFiles(sourceRoot).map((file) => ({
      from: path.relative(root, file),
      to: path.join(destinationRoot, path.relative(sourceRoot, file)),
    })),
  };
}

function inspectInstallTarget(plan) {
  const expected = plan.wouldCopyFiles.map((item) => ({
    relativePath: path.relative(plan.destinationRoot, item.to),
    from: item.from,
    to: item.to,
    sourceSha256: sha256File(path.join(repoRoot, item.from)),
    destinationExists: fs.existsSync(item.to),
    destinationSha256: fs.existsSync(item.to) ? sha256File(item.to) : null,
  }));
  const missing = expected.filter((item) => !item.destinationExists).map((item) => item.relativePath);
  const stale = expected
    .filter((item) => item.destinationExists && item.sourceSha256 !== item.destinationSha256)
    .map((item) => item.relativePath);
  const expectedRelative = new Set(expected.map((item) => item.relativePath));
  const extra = fs.existsSync(plan.destinationRoot)
    ? collectFiles(plan.destinationRoot)
        .map((file) => path.relative(plan.destinationRoot, file))
        .filter((file) => !expectedRelative.has(file))
    : [];
  return {
    destinationExists: fs.existsSync(plan.destinationRoot),
    matchesPackage: fs.existsSync(plan.destinationRoot) && missing.length === 0 && stale.length === 0,
    missing,
    stale,
    extra,
  };
}

function buildPackageFingerprint(sourceRoot, sourceRelativeRoot) {
  const files = collectFiles(sourceRoot).map((file) => ({
    path: path.join(sourceRelativeRoot, path.relative(sourceRoot, file)),
    sha256: sha256File(file),
  }));
  const digest = crypto.createHash("sha256");
  for (const file of files) {
    digest.update(file.path);
    digest.update("\0");
    digest.update(file.sha256);
    digest.update("\0");
  }
  return {
    algorithm: "sha256",
    digest: digest.digest("hex"),
    fileCount: files.length,
    files,
  };
}

function parseFrontmatter(text) {
  const match = text.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const out = {};
  for (const line of match[1].split(/\r?\n/)) {
    const index = line.indexOf(":");
    if (index === -1) continue;
    const key = line.slice(0, index).trim();
    const value = line.slice(index + 1).trim();
    out[key] = value;
  }
  return out;
}

function sameSet(actual, expected) {
  return actual.length === expected.length && expected.every((item) => actual.includes(item));
}

function findForbiddenHits(root, markers) {
  const hits = [];
  for (const file of collectTextFiles(root)) {
    const rel = path.relative(root, file);
    if (rel === "README.md") continue;
    if (rel === "core-release-manifest.json") continue;
    if (rel === "scripts/validate-core-package.mjs") continue;
    if (rel === "skills/immersive-motion-ui/references/library-integration.md") continue;
    if (rel.startsWith("skills/immersive-motion-ui/scripts/")) continue;
    if (rel.startsWith("tests/forward-evals/fixtures/")) continue;
    const text = fs.readFileSync(file, "utf8");
    for (const marker of markers) {
      // Tests and the bounded Core showcase may reference checked local fixtures; root assets remain forbidden.
      if (marker === "assets/" && (rel.startsWith("tests/") || rel.startsWith("skills/immersive-motion-ui/examples/minimal-showcases/"))) continue;
      if (text.includes(marker)) hits.push(`${rel}: ${marker}`);
    }
  }
  return hits;
}

function collectTextFiles(root) {
  const output = [];
  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    if (entry.name === ".git" || entry.name === "node_modules") continue;
    const next = path.join(root, entry.name);
    if (entry.isDirectory()) output.push(...collectTextFiles(next));
    if (entry.isFile() && /\.(md|json|ya?ml|mjs|js|ts|tsx)$/.test(entry.name)) output.push(next);
  }
  return output;
}

function collectFiles(root) {
  const output = [];
  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const next = path.join(root, entry.name);
    if (entry.isDirectory()) output.push(...collectFiles(next));
    if (entry.isFile()) output.push(next);
  }
  return output.sort();
}

function uniqueSorted(items) {
  return [...new Set(items)].sort();
}

function sha256File(file) {
  return crypto.createHash("sha256").update(fs.readFileSync(file)).digest("hex");
}
